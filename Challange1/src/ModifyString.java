public class ModifyString {
	
	public static void main(String[] args) {
		
		String myName = "Kimberly Tse";		//My name
		myName = myName.toLowerCase();		//My name in lowercase
		int lengthMyName = myName.length();	//Number of characters (including space).
		
		System.out.println(myName + " has " + lengthMyName + " characters. " ); // Prints my name and number of characters
	}

}